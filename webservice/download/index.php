                <h2>Android Application Instructions</h2>
                <h3>Application Description</h3>
                <p>The Negotiating Consent Android application allows you to share personal data from your Android device in return for points. You will receive requests to share various types of data through question notifications. You will be given the opportunity to review a sample of the collected data before deciding whether to share and collect the points.</p>
                <h3>Requirements</h3>
                <ul>
                    <li>An internet enabled Android phone or tablet</li>
                    <li>Android version 4.4 (Kit Kat) and above</li>
                    <li>Web browser installed on phone ie: Chrome, Opera, Firefox, or other pre-installed browser app</li>
                </ul>
                <h3>Installation</h3>
                <ol>
                    <li>Ensure your device is able to install apps from outside the Google Play store by choosing &quot;Settings&quot;, then tap &quot;Security&quot;, then enable the option &quot;Unknown Sources&quot;. More details available <a href="http://www.cnet.com/uk/how-to/how-to-install-apps-outside-of-google-play/">here</a></li>
                    <li>Tap the download button situated below these instructions and the download of the application will begin. Your browser may display a warning that this type of file may be harmful, this app is completely safe to use, so please tap &quot;OK&quot;.</li>
                    <li>The application package will by default be saved to your &quot;Downloads&quot; folder of your device. Open download folder, find the file with the name &quot;app_release.apk&quot; and tap it. Alternately tap on the download icon on the notification bar on your device.</li>
                    <li>A permission screen will appear, please read the permissions terms, tap the button marked &quot;Next&quot;, read the additional information, then tap the button marked  &quot;Install&quot;.</li>
                    <li>If the installation was successful, a confirmation screen will be shown with two buttons.</li>
                    <li>Tap the &quot;Done&quot; button to finish the installation.</li>
