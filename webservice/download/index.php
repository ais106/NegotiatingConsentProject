                <h3>Using the Application</h3>
                <h4>Registration</h4>
                <p>Please ensure that internet is available before using the application. When opening the application for the first time you will be asked if you would like to begin using the application, please tap &quot;Yes&quot; if you would like to continue. Please read the text on the next screen and tap the button to confirm that you understand. Your device will be registered and you will start to receive notification containing questions.</p>
                <h4>Answering Question Notifications</h4>
                <p>You will receive notifications with a question mark icon which will appear at the top of your device screen. Swipe down from the top of the screen to reveal the notification, tap it and a question screen will appear. The screen displays a question concerning the sharing of data stored on your device and the total points in your account. Each question has allocated a number of points, answering &quot;Yes&quot; to a question will add the points to your account pending future confirmation. Please read the question, then answer by tapping the appropriate answer button. Once the question is answered the status screen will show which data type has been collected and points earned.</p>
                <h4>Reviewing Collected Data</h4>
                <p>After several questions have been sent to you, another different notification will appear marked as &quot;Survey Alert&quot;. Tap the notification to show the review screen. The review screen will display a sample of each type of data that has been collected from the device. You can choose to share the data and add the points to your account by tapping the button marked &quot;Share Data&quot; or withdraw your permission to share the data by tapping the &quot;Cancel&quot; button.</p>               
